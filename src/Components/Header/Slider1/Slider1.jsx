



import sliderCss from "./slider1.module.css";

const text =
  "Только практический опыт студийных проектов и важные новости рынка ";
// Пробел в конце строки нужен ТОЛЬКО для визуального разделения при повторе
const RunningLine = () => {
  // Создаём 2 одинаковых блока для бесшовной анимации
  const content = `${text} `.repeat(3).trim();
  //  Берёт строку text и добавляет к ней пробел (${text} ).
  //  Повторяет её 3 раза с помощью .repeat(3).
  //  Например, если text = "A", то получится "A A A ".
  //  Обрезает лишние пробелы в конце с помощью .trim(). (Итог: "A A A".)

  return (
    //  Внешний контейнер (wrapper) = Это корневой элемент, который ограничивает область видимости бегущей строки. В стилях для него обычно задаётся:
    // .wrapper {
    //   width: 100%;
    //   overflow: hidden; /* Скрывает всё, что выходит за границы */
    // }
    <div className={sliderCss.wrapper}>
      {/* Это элемент, который будет двигаться (анимироваться) */}
      <div className={sliderCss.container}>
        {/* Два одинаковых блока с текстом. Например, если content = "A A A", то получится: [A A A][A A A] */}
        <div className={sliderCss.content}>{content}</div>
        <div className={sliderCss.content} aria-hidden="true">
          {/* aria-hidden="true" — скрывает второй блок для скринридеров (чтобы не дублировался для accessibility). */}
          {content}
          {/* Почему два блока? = Чтобы создать эффект бесконечности:

           Первый блок уезжает влево.
           Второй блок его заменяет.

           В момент, когда первый блок полностью скрылся, анимация незаметно перескакивает назад, и цикл повторяется. */}
        </div>
      </div>
    </div>
  );
};

export default RunningLine;